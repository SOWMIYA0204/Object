let salaries={
    John:100,
    Sowmi:200,
    Sindhu:300,
};
//dot syntax
console.log(salaries.John);

//box syntax
let salaries={
    John:100,
    Sowmi:200,
    Sindhu:300,
};
console.log(salaries["Sowmi"]);

//object.keys
let salaries={
    John:100,
    Sowmi:200,
    Sindhu:300,
};
console.log(Object.keys(salaries));

//Object.values
let salaries={
    John:100,
    Sowmi:200,
    Sindhu:300,
};
console.log(Object.values(salaries));
